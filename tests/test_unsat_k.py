import pytest

import numpy as np
import numpy.testing as npt

from rosetta.rosetta import UnsaturatedK

# [theta_r, theta_s, alpha, npar]
reference_inputs = [
    [0.11393417936656314, 0.44355925679893304, 0.01780135103972393, 1.1753486682703473],
    [0.0963124308866378, 0.4585250296162462, 0.012964516503391586, 1.3366463550396503],
    [0.048704905015365695, 0.4332694551100699, 0.009242605757396738, 1.6355321344832925],
    [0.09271596382671454, 0.4346262793538728, 0.028992881236195207, 1.1893642827993154],
    [0.025912727428977916, 0.32930183787586276, 0.023879099446067083, 1.4137655626271022],
    [0.0544870159557327, 0.38433499737110727, 0.030215927535637614, 4.705007584449611],
]

# [logKo, lpar]
expected_means = [
    [0.3754950103043596, -3.4749238500959376],
    [0.44101244848056326, -0.9130697925783309],
    [0.6360363198592237, 0.1377264830785368],
    [0.7959777471859515, -3.5535598185165616],
    [1.1188048936706472, -0.8886686004322493],
    [1.5575486963390446, -0.8403693588432106],
]

# [logK0_std, lpar_std]
expected_stds = [
    [0.28361232370777933, 1.8339990897683196],
    [0.24848371865297278, 1.1938070901178723],
    [0.25728730028311775, 0.9952747511681211],
    [0.285940463806345, 1.666148301609446],
    [0.24567197465378665, 0.9656256891198982],
    [0.294269173548014, 0.6473923582804932],
]

def test_unsatk():
    model = UnsaturatedK()
    boot_result = model.predict(reference_inputs)
    means = np.mean(boot_result, axis=0)
    stds = np.std(boot_result, axis=0, ddof=1)

    npt.assert_allclose(means, expected_means, rtol=1e-2, atol=1e-2)
    npt.assert_allclose(stds, expected_stds, rtol=1e-2, atol=1e-2)

